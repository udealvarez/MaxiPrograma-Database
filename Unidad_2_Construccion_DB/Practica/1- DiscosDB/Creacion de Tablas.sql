	Id INT PRIMARY KEY,
	Titulo NVARCHAR(50),
	FechaLanzamiento DATE,
	CantidadCanciones INT,
	UrlImagenTapa NVARCHAR(100),
	IdEstilo INT,
	IdTipoEdicion INT
)