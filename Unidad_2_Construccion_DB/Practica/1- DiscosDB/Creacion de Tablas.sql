CREATE TABLE TIPOSEDICION(
	Id INT PRIMARY KEY,
	Descripcion NVARCHAR(100)
)

CREATE TABLE ESTILOS(
	Id INT PRIMARY KEY,
	Descripcion NVARCHAR(100)
)

